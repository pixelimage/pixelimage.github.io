
var FaIconList = (function(){
	var view;
	var v = {};
	
	/* ---------- ---------- ---------- */
	//初期化
	
	function init(){
		view = $('#FaIconList');
		stageInit();
		createlayout();
		setBtn();
		// stageIn();
	}
	
	/* ---------- ---------- ---------- */
	//レイアウト作成・イベントアサイン
	
	function createlayout(){
		
		v.listArea = view.find(".replaceArea");
		
	//モーダル
		// $('.btn_icon_open').click(function(event){ 
		// 	stageIn();
		// 	return false;
		// });
		view.find('.btn_close').click(function(){ 
			stageOut();
			return false;
		});
		
		
		view.on("click",'.fa',function(){ 
			// var s = $(this).data("id") + " " + sizeCs[currentSize];
			selectIcon ($(this).data("id"))
		});
	}
	function selectIcon(_s){
		if(callback){
			callback(_s);
		} 
		stageOut();
	}
	
	function setBtn(){
	}
	
	
	/* ---------- ---------- ---------- */
	//個別処理
	
	
	function update(){
		var tag = ""
		for (var i = 0; i < fontList.length ; i++) {
			tag += "<h2>"+fontList[i].title+"</h2>"
			tag += '<div class="_g">'
			for (var ii = 0; ii < fontList[i].list.length ; ii++) {
				// if(fontList[i].list[ii].indexOf(searchString) != -1){
					var s = fontList[i].list[ii][0] //+ " " + sizeCs[currentSize];
					tag += '<span><i class="fa fa-fw fa-'+ s +'" data-id="'+fontList[i].list[ii][1]+'"></i></span>'
				// }
			}
			tag += '</div>'
		}
		v.listArea.html(tag);
	}
	
	/* ---------- ---------- ---------- */
	//表示・非表示処理
	
	var isOpen = false;
	var isFirst = true;
	function stageInit(){
		view.hide();
	}
	var callback
	function stageIn(_callback){
		if(! isOpen){ isOpen = true;
			view.show();
			callback = _callback;
			if(isFirst){
				update();
			}
			isFirst = false;
		}
	}
	function stageOut(){
		if(isOpen){ isOpen = false;
			view.hide();
		}
	}

	return { init:init, stageIn:stageIn, stageOut:stageOut }
})();

